export const getSpriteData = (userData: UserData): Promise<SpriteData> => {
    let data: SpriteData
    if (userData.scene === 'MENU') {
        data = spriteDataMenu
    } else if (userData.scene === 'PLAY') {
        data = spriteDataPlay
    }
    return new Promise(resolve => resolve(data))
}

